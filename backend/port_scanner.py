@dataclass(frozen=True, slots=True)
class PortResult:
    port: int
    service: str
    state: Literal["open", "closed", "filtered"]
    banner: str | None = None


@dataclass(frozen=True, slots=True)
class ScanReport:
    target: str
    resolved_ip: str
    ports_scanned: int
    open_ports: int
    results: list[PortResult]
    duration_seconds: float


def _get_service(port: int) -> str:
    """Resolve port number to a service name."""
    if port in COMMON_PORTS:
        return COMMON_PORTS[port]
    if port <= 49151:
        try:
            return socket.getservbyport(port, "tcp")
        except (OSError, ValueError):
            return "unknown"
    return "unknown"


async def _scan_port(
    host: str,
    port: int,
    timeout: float = 2.0,
    grab_banner: bool = False,
) -> PortResult:
    """Try to connect to a single TCP port and optionally grab a banner."""
    service = _get_service(port)
    try:
        _, writer = await asyncio.wait_for(
            asyncio.open_connection(host, port),
            timeout=timeout,
        )
        banner: str | None = None
        if grab_banner:
            try:
                writer.write(b"\r\n")
                await writer.drain()
                reader, _ = await asyncio.open_connection(host, port)
                banner_data = await asyncio.wait_for(reader.read(256), timeout=timeout)
                if banner_data:
                    banner = banner_data.decode("utf-8", errors="replace").strip()[:120]
            except Exception:
                pass
        writer.close()
        await writer.wait_closed()
        return PortResult(port=port, service=service, state="open", banner=banner)
    except (asyncio.TimeoutError, ConnectionRefusedError, OSError):
        return PortResult(port=port, service=service, state="closed")
    except Exception:
        return PortResult(port=port, service=service, state="filtered")


async def scan(
    host: str,
    ports: list[int] | None = None,
    *,
    timeout: float = 2.0,
    concurrency: int = 100,
    grab_banner: bool = False,
) -> ScanReport:
    """
    Scan target host for open TCP ports.

    Args:
        host: IP or hostname to scan.
        ports: List of ports. If None, scans COMMON_PORTS.
        timeout: Seconds per connect attempt.
        concurrency: Max simultaneous connections.
        grab_banner: Whether to attempt banner grabbing.

    Returns a ScanReport with all results.
    """
    if ports is None:
        ports = sorted(COMMON_PORTS.keys())

    # Resolve hostname
    try:
        resolved_ip = socket.gethostbyname(host)
    except socket.gaierror:
        resolved_ip = host

    semaphore = asyncio.Semaphore(concurrency)
    start = asyncio.get_event_loop().time()

    async def _limited(p: int) -> PortResult:
        async with semaphore:
            return await _scan_port(resolved_ip, p, timeout=timeout, grab_banner=grab_banner)

    tasks = [_limited(p) for p in ports]
    results = await asyncio.gather(*tasks)

    duration = asyncio.get_event_loop().time() - start
    open_ports = [r for r in results if r.state == "open"]

    return ScanReport(
        target=host,
        resolved_ip=resolved_ip,
        ports_scanned=len(ports),
        open_ports=len(open_ports),
        results=open_ports,  # Only open ports in results
        duration_seconds=round(duration, 2),
    )


def report_to_mirv_findings(report: ScanReport) -> list[dict]:
    """Convert a ScanReport into MIRV findings list."""
    if report.open_ports == 0:
        return [{
            "tool": "port-scan",
            "severity": "info",
            "title": f"No open ports found on {report.target} ({report.resolved_ip})",
            "detail": (
                f"Target: {report.target}\n"
                f"Resolved IP: {report.resolved_ip}\n"
                f"Ports scanned: {report.ports_scanned}\n"
                f"Duration: {report.duration_seconds}s"
            ),
            "target": report.target,
            "type": "tech",
        }]

    findings = []
    for r in sorted(report.results, key=lambda x: x.port):
        sev = "high" if r.port in (22, 23, 3389, 5900, 5901) else \
              "medium" if r.port in (21, 25, 53, 80, 443, 445, 1433, 3306, 5432, 6379, 27017, 8080, 8443) else \
              "low"
        banner_info = f"\nBanner: {r.banner}" if r.banner else ""
        findings.append({
            "tool": "port-scan",
            "severity": sev,
            "title": f"Port {r.port}/{r.service} — OPEN",
            "detail": (
                f"Port: {r.port}\n"
                f"Service: {r.service}\n"
                f"State: {r.state}{banner_info}"
            ),
            "target": report.target,
            "type": "vuln" if sev == "high" else "tech",
            "extra": {
                "port": r.port,
                "service": r.service,
                "banner": r.banner,
            },
        })

    # Summary finding
    high = sum(1 for r in report.results if r.port in (22, 23, 3389, 5900, 5901))
    med = sum(1 for r in report.results if r.port in (21, 25, 53, 80, 443, 445, 1433, 3306, 5432, 6379, 27017, 8080, 8443))
    findings.append({
        "tool": "port-scan",
        "severity": "info",
        "title": f"Scan complete — {report.open_ports} open ports ({high} high, {med} medium)",
        "detail": (
            f"Target: {report.target}\n"
            f"Resolved IP: {report.resolved_ip}\n"
            f"Ports scanned: {report.ports_scanned}\n"
            f"Open ports: {report.open_ports}\n"
            f"Duration: {report.duration_seconds}s"
        ),
        "target": report.target,
        "type": "tech",
        "extra": {"open_ports": report.open_ports, "duration": report.duration_seconds},
    })

    return findings
